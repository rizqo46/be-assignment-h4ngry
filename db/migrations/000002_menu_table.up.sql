CREATE TABLE menus (
	id serial4 NOT NULL,
	"uuid" uuid NOT NULL DEFAULT uuid_generate_v1mc(),
	"name" TEXT NOT NULL,
	price int4 NOT NULL,
	image TEXT NOT NULL,
	description TEXT NOT NULL,
	created_at timestamp NOT NULL DEFAULT now(),
	src_doc tsvector NOT NULL GENERATED ALWAYS AS (to_tsvector('simple'::regconfig,
(name || ' '::TEXT) || description)) STORED,
	CONSTRAINT menus_pkey PRIMARY KEY (id),
	CONSTRAINT menus_uuid_key UNIQUE (uuid)
);

CREATE INDEX menu_src_doc_idx ON
menus
  USING GIN(src_doc);

